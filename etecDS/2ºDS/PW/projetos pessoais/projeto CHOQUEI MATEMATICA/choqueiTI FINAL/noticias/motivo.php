        <section class="motivo" style="display: flex; flex-direction: column; align-items: center;">
            <h1 id="motivo" class="titulo texto-central">Motivo</h1>
            <p class="texto">
                Esse projeto, ou seja, o site, foi idealizado como um registrador. Nós,
                desenvolvedores do site e integrantes do grupo, espalhamos uma notícia falsa onde o 3º D.S supostamente
                não ganhou a edição 2023 do evento Semana Paulo Freire. A ideia é descobrir quantas pessoas seriam
                alcançadas por essa fake news e, em seguida, criar uma fórmula matemática ou um gráfico para mostrar o
                alcance que essa notícia falsa teve. <br><br>
                O motivo por trás desse projeto foi entender melhor como informações falsas se espalham e afetam as
                pessoas nos tempos atuais. Sabemos que a internet possibilita a rápida disseminação de conteúdos
                enganosos, então quisemos investigar como uma notícia falsa, relacionada a um evento importante na ETEC
                Cidade Tiradentes, poderia se espalhar e influenciar a percepção das pessoas. <br><br></p>
            <img src="./img/desenvolvedores01-fakeNews.jpeg" class="img-fluid " style="max-height: 200px;" alt=""><br><br>

            <p class="texto">Criamos um site, com o nome de CHOQUEI T.I, inspirado na página do Twitter CHOQUEI,
                para divulgar essa notícia falsa e acompanharmos de perto como ela se propaga.
                Procuramos ser ousado com uma notícia que chamaria atenção, mas sempre deixando claro que é uma notícia
                falsa. <br><br></p>
            <img src="./img/logo.png" class="img-fluid" style="max-height: 200px;" alt=""><br><br>
            <p class="texto">Com essa abordagem, esperamos obter insights sobre como informações erradas se espalham,
                entender as
                variáveis que influenciam esse processo e, por fim, usar uma fórmula matemática, baseada na função
                exponencial, para representar graficamente a disseminação e o impacto dessa notícia falsa.<br><br>
                Todo o projeto foi feito para a disciplina de Matemática, com o professor orientador Oswaldo Freitas.
            </p>
